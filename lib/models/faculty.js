import mongoose from "mongoose";
const { Schema } = mongoose;
const facultySchema =new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
  },
  username: {
    type: String,
  },
  gender: {
    type: String,
  },
  designation: {
    type: String,
    required: true,
  },
  branch: {
    type: String,
    required: true,
  },
  contactNumber: {
    type: Number,
  },
  dob: {
    type: String,
    required: true,
  },
  joiningYear: {
    type: Number,
    required: true,
  },
  passwordUpdated: {
    type: Boolean,
    default: false,
  },
});

let facultyModel
if (!mongoose.models.faculty)
 facultyModel = mongoose.model("faculty", facultySchema);

console.log(mongoose.models.faculty)

export default  mongoose.models.faculty || facultyModel;